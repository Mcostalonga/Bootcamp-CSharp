namespace ExemploPOO.Models
{
    public class Calculadora
    {
        public int Somar(int num1, int num2) // Método 1
        {
            return num1 + num2;
        }
        public int Somar(int num1, int num2, int num3) // Método 2
        {
            return num1 + num2 + num3;
        }
    }
}